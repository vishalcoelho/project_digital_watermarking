function ret_dec=mat2dec(arg_mat)
%--------------------------------------------------------------------------
ret_dec=0;
pos=0;
for i=8:-1:1
    if arg_mat(i)==1
        ret_dec=ret_dec+power(2,pos);
    end
    pos=pos+1;
end
